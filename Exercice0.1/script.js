//Création de 2 variables
let nombre1 = 5
let nombre2 = 6

//Création d'un if pour comparer les 2 nombres
// Si nombre1 = nombre2 --> j'affiche "Ce sont les mêmes"
if (nombre1 === nombre2) {
    console.log("Ce sont les mêmes !!");
// Si nombre1 > nombre2 --> j'affiche "nombre1 > nombre2"
} else if (nombre1 > nombre2) {
    console.log(nombre1 + " > " + nombre2);
// tout les autres cas soit nombre1 < nombre2 --> j'affiche "nombre1 < nombre2"
} else {
    console.log(nombre1 + " < " + nombre2);
}
